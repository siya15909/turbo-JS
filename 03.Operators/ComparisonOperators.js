//COMPARISON OPERATIONS
a=90;//number
b="90";//string
console.log("Is a>b :",a>b);//a greater than b
console.log("Is a<b :",a<b);//a less than b
console.log("Is a==b:",a==b);//Not strict not datatype only the value is checked
console.log("Is a!=b:",a!=b);//a not equals b
console.log("Is a>=b:",a>=b);//a greater than or equals b
console.log("Is a<=b:",a<=b);//a less than or equals b
console.log("Is a===b:",a===b);//Strict comparison datatype also checked
console.log("Is a!==b:",a!==b);//Strict comparison datatype also checked